package Tulasi;

public class multiarray {
    public static void main(String[] args){

        int[][] arr={{3,5},{8,2}};
        for(int i=0;i<2;i++){
            for(int j=0;j<2;j++)
            {
                System.out.print(arr[i][j] + " ");
            }
            System.out.println("");
        }

    }
}
