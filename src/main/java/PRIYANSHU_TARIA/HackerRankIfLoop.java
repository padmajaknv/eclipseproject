package PRIYANSHU_TARIA;

public class HackerRankIfLoop
{
    public static void main(String[] args)
    {
        int x=2;
        System.out.println("Multiplication table of "+x+" is ");
        for(int i=1;i<=10;i++)
        {
            System.out.println(x + " * "+ i +" = " + x*i);
        }
    }
}
